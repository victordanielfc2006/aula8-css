let nu1 = 20;
let nu2 = 150;

// operador de comparação ==
console.log(nu1 == nu2);
// operador de comparação diferente !=
console.log(nu1 != nu2);
// operador de comparação de valores e tipos ===
console.log(nu1 === nu2);

console.log(nu1 > nu2); //false

console.log(nu1 >= nu2); //false

console.log(nu1 < nu2); //true

console.log(nu1 <= nu2); //true

